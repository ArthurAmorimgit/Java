package model;

public class Especialidade {
    private Integer cod_especialidade;
    private String nome_especialidade;
    private String descricao_especialidade;

    public Integer getCod_especialidade() { return cod_especialidade; }
    public void setCod_especialidade(Integer cod_especialidade) { this.cod_especialidade = cod_especialidade; }
    public String getNome_especialidade() { return nome_especialidade; }
    public void setNome_especialidade(String nome_especialidade) { this.nome_especialidade = nome_especialidade; }
    public String getDescricao_especialidade() { return descricao_especialidade; }
    public void setDescricao_especialidade(String descricao_especialidade) { this.descricao_especialidade = descricao_especialidade; }
}
