package model;

public class Recepcionista {
    private Integer cod_recepcionista;
    private String nome_recepcionista;
    private String cpf;

    public Integer getCod_recepcionista() { return cod_recepcionista; }
    public void setCod_recepcionista(Integer cod_recepcionista) { this.cod_recepcionista = cod_recepcionista; }
    public String getNome_recepcionista() { return nome_recepcionista; }
    public void setNome_recepcionista(String nome_recepcionista) { this.nome_recepcionista = nome_recepcionista; }
    public String getCpf() { return cpf; }
    public void setCpf(String cpf) { this.cpf = cpf; }
}
