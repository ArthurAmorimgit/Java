package model;

public class Convenio {
    private Integer cod_convenio;
    private String nome_convenio;
    private String cobertura_convenio;
    private String telefone_convenio;

    public Integer getCod_convenio() { return cod_convenio; }
    public void setCod_convenio(Integer cod_convenio) { this.cod_convenio = cod_convenio; }
    public String getNome_convenio() { return nome_convenio; }
    public void setNome_convenio(String nome_convenio) { this.nome_convenio = nome_convenio; }
    public String getCobertura_convenio() { return cobertura_convenio; }
    public void setCobertura_convenio(String cobertura_convenio) { this.cobertura_convenio = cobertura_convenio; }
    public String getTelefone_convenio() { return telefone_convenio; }
    public void setTelefone_convenio(String telefone_convenio) { this.telefone_convenio = telefone_convenio; }
}
